/**
 * Enum representing the types of links available for a platform.
 * The two types are:
 * - `WEB`: Represents a web-based link.
 * - `MOBILE`: Represents a mobile application-based link.
 */
export enum LinkType {
  /** Web-based link */
  WEB = 'web',

  /** Mobile application-based link */
  MOBILE = 'mobile',
}
